# Day 1 - Learning Variables
# This program asks for your name, calculates its length,
# and greets you with the result.

# Ask the user for their name and store it
name = input("What is your name? ")

# Calculate the length of the name
name_length = len(name)

# Greet the user by name
print("Hello " + name + "!")

# Tell the user how many characters their name has
print("Your name is", name_length, "characters long.")
